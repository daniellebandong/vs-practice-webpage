using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using vs_practice_webpage.Data;
using vs_practice_webpage.Models;

namespace vs_practice_webpage.Pages.Movies
{
    public class DetailsModel : PageModel
    {
        private readonly vs_practice_webpage.Data.vs_practice_webpageContext _context;

        public DetailsModel(vs_practice_webpage.Data.vs_practice_webpageContext context)
        {
            _context = context;
        }

      public Movie Movie { get; set; } = default!; 

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Movie == null)
            {
                return NotFound();
            }

            var movie = await _context.Movie.FirstOrDefaultAsync(m => m.Id == id);
            if (movie == null)
            {
                return NotFound();
            }
            else 
            {
                Movie = movie;
            }
            return Page();
        }
    }
}
